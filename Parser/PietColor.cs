namespace Esolang.Piet;

/// <summary>
/// Represents the normalized Piet codel colors used by the language.
/// </summary>
public enum PietColor : byte
{
    /// <summary>
    /// Blocking black codel.
    /// </summary>
    Black = 0,
    /// <summary>
    /// Sliding white codel.
    /// </summary>
    White,
    /// <summary>
    /// Light red command codel.
    /// </summary>
    LightRed,
    /// <summary>
    /// Red command codel.
    /// </summary>
    Red,
    /// <summary>
    /// Dark red command codel.
    /// </summary>
    DarkRed,
    /// <summary>
    /// Light yellow command codel.
    /// </summary>
    LightYellow,
    /// <summary>
    /// Yellow command codel.
    /// </summary>
    Yellow,
    /// <summary>
    /// Dark yellow command codel.
    /// </summary>
    DarkYellow,
    /// <summary>
    /// Light green command codel.
    /// </summary>
    LightGreen,
    /// <summary>
    /// Green command codel.
    /// </summary>
    Green,
    /// <summary>
    /// Dark green command codel.
    /// </summary>
    DarkGreen,
    /// <summary>
    /// Light cyan command codel.
    /// </summary>
    LightCyan,
    /// <summary>
    /// Cyan command codel.
    /// </summary>
    Cyan,
    /// <summary>
    /// Dark cyan command codel.
    /// </summary>
    DarkCyan,
    /// <summary>
    /// Light blue command codel.
    /// </summary>
    LightBlue,
    /// <summary>
    /// Blue command codel.
    /// </summary>
    Blue,
    /// <summary>
    /// Dark blue command codel.
    /// </summary>
    DarkBlue,
    /// <summary>
    /// Light magenta command codel.
    /// </summary>
    LightMagenta,
    /// <summary>
    /// Magenta command codel.
    /// </summary>
    Magenta,
    /// <summary>
    /// Dark magenta command codel.
    /// </summary>
    DarkMagenta,
}
